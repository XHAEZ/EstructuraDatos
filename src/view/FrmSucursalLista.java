    private void btnVentaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVentaActionPerformed
        // TODO add your handling code here:
        int fila = tblTabla.getSelectedRow();
        if (fila >= 0) {
            if (modelo.getDatos() != null) {
                try {
                    this.sucursal.setSucursal(modelo.getDatos().get(fila));
                } catch (EmptyException ex) {
                    Logger.getLogger(FrmSucursalLista.class
                            .getName()).log(Level.SEVERE, null, ex);
                } catch (PositionException ex) {
                    Logger.getLogger(FrmSucursalLista.class
                            .getName()).log(Level.SEVERE, null, ex);
                }
                new FrmVentasLista(null, true, this.sucursal).setVisible(true);
                cargarTabla();
                tblTabla.clearSelection();
            } else {
                JOptionPane.showMessageDialog(null, "Ingrese un nombre a la sucursal", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Seleccione una sucursal", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnVentaActionPerformed

