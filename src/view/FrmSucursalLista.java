    private void cargarTabla() {
        modelo.setDatos(sucursal.getSucursales());
        tblTabla.setModel(modelo);
        tblTabla.updateUI();

    }

    private void limpiar() {
        this.sucursal.setSucursal(null);
        txtNombre.setText("");
        cargarTabla();
    }

    private void mostrarSucursalMayorVentas() throws EmptyException, PositionException {
        try {
            int posicionSucursal = sucursal.obtenerSucursalMayorVentas();
            Sucursal sucursalMayorVentas = sucursal.getSucursales().get(posicionSucursal);
            txtVenta.setText("La sucursal con mayores ventas es: " + sucursalMayorVentas.getNombre());
        } catch (EmptyException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage(), "ERROR", JOptionPane.ERROR_MESSAGE);

        }
    }

    private void mostrarMenoresVentas() {
        try {
            String resultado = "El mes con las menores ventas es: " + sucursal.obtenerMesMenoresVentas().toString();
            txtVenta.setText(resultado);
        } catch (EmptyException | PositionException e) {
            txtVenta.setText("No se encontraron ventas registradas.");
        }
    }

    private void guardarSucurusal() {

        if (txtNombre.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Por favor ingrese todos los datos necesarios", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            try {
                this.sucursal.getSucursal().setNombre(txtNombre.getText());
                sucursal.registrar();
                sucursal.setSucursal(null);
                limpiar();
                JOptionPane.showMessageDialog(null, "Se guardo correctamente", "Confirmacion", JOptionPane.INFORMATION_MESSAGE);
            } catch (SpaceException ex) {
                JOptionPane.showMessageDialog(null, ex.getMessage(), "PENDEJO", JOptionPane.ERROR_MESSAGE);
            }
        }
    }

